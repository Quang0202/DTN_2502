package Exercise1.entity;

public class MyMath {
    public static double max(double a, double b) {
        return (a > b) ? a : b;
    }

    public static double min(double a, double b) {
        return (a < b) ? a : b;
    }

    public static double sum(double a, double b) {
        return a + b;
    }
}
