package com.programming.nhatanh.lesson7.entity;

public class MyMath {
    public int min(int a, int b){
        if(a > b){
            return b;
        } else if ( a < b ) {
            return a;
        } else {
            return a;
        }
    }

    public int sum(int a, int b){
        return a + b;
    }
}
